public class ShipGenerator {
    ShipGenerator(double generating_time, double ship_capacity_min, double ship_capacity_max) {
        this.generating_time = generating_time;
        this.ship_capacity_min = ship_capacity_min;
        this.ship_capacity_max = ship_capacity_max;
    }

    private double generating_time;
    private double ship_capacity_min;
    private double ship_capacity_max;


}
